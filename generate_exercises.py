def catalog_category_key(subject: str, category: str) -> str:
    return f"{subject.casefold()}::{category.casefold()}"


def existing_category_lookup(site_root: Path) -> dict[str, dict[str, Any]]:
    catalog = load_existing_root_catalog(site_root)
    lookup: dict[str, dict[str, Any]] = {}

    for category in catalog.get("categories") or []:
        if not isinstance(category, dict):
            continue
        name = str(category.get("name") or "").strip()
        if not name:
            continue
        subject = str(category.get("subject") or infer_subject_from_category_name(name) or "Fisica").strip()
        lookup[catalog_category_key(subject, name)] = category

    return lookup


def infer_subject_from_category_name(category_name: str) -> str:
    math_names = {
        "algebra",
        "analisi",
        "geometria",
        "geometria analitica",
        "limiti",
        "trigonometria",
        "probabilita",
        "probabilita e statistica",
        "statistica",
    }
    normalized = (
        category_name.casefold()
        .replace("à", "a")
        .replace("è", "e")
        .replace("é", "e")
        .replace("ì", "i")
        .replace("ò", "o")
        .replace("ù", "u")
    )
    return "Matematica" if normalized in math_names else "Fisica"


def iter_collection_manifests(site_root: Path) -> list[Path]:
    manifests: list[Path] = []

    for subject_slug in sorted(ALLOWED_ROOTS):
        subject_dir = site_root / subject_slug
        if not subject_dir.exists():
            continue

        for path in sorted(subject_dir.rglob("*.json"), key=lambda item: natural_key(item.relative_to(site_root).as_posix())):
            if path.name.casefold() == "collection.json":
                continue
            if is_exercise_manifest(path):
                manifests.append(path)

    return manifests


def collection_entry_from_manifest(site_root: Path, manifest_path: Path, index: int) -> dict[str, Any] | None:
    manifest = load_json(manifest_path)
    if not manifest:
        return None

    rel_dir = PurePosixPath(manifest_path.parent.relative_to(site_root).as_posix())
    parts = rel_dir.parts
    if not parts or parts[0] not in ALLOWED_ROOTS:
        return None

    exercises = manifest.get("exercises") if isinstance(manifest.get("exercises"), list) else []
    if not exercises:
        return None

    inferred = infer_path_metadata(rel_dir)
    subject_info = SUBJECTS.get(parts[0], {})
    title = str(manifest.get("title") or inferred["title"])
    subject = str(manifest.get("subject") or subject_info.get("name") or inferred["subject"])
    category = str(manifest.get("category") or inferred["category"] or subject)
    topic = str(manifest.get("topic") or inferred["topic"] or title)
    description = str(
        manifest.get("description")
        or f"Raccolta con {len(exercises)} esercizi svolti."
    )
    order = normalize_order(manifest.get("order"))
    if order is None:
        order = (index + 1) * 10

    return {
        "id": rel_dir.as_posix().replace("/", "-"),
        "title": title,
        "description": description,
        "subject": subject,
        "category": category,
        "topic": topic,
        "icon": manifest.get("icon") or "",
        "order": order,
        "tags": unique_list([manifest.get("tags"), *(exercise.get("tags") for exercise in exercises)], limit=12),
        "level": manifest.get("level") or "",
        "schoolYear": manifest.get("schoolYear") or "",
        "estimatedTime": manifest.get("estimatedTime") or f"{len(exercises)} esercizi",
        "file": f"{rel_dir.as_posix()}/index.html",
        "manifest": f"{rel_dir.as_posix()}/{manifest_path.name}",
        "count": len(exercises),
        "isCollection": True,
        "isWip": bool(manifest.get("isWip", False)),
    }


def build_global_catalog(site_root: Path) -> dict[str, Any]:
    entries = [
        entry
        for index, manifest_path in enumerate(iter_collection_manifests(site_root))
        if (entry := collection_entry_from_manifest(site_root, manifest_path, index)) is not None
    ]

    subject_order = {info["name"]: info["order"] for info in SUBJECTS.values()}
    entries.sort(
        key=lambda entry: (
            subject_order.get(entry["subject"], 999999),
            entry["category"].casefold(),
            normalize_order(entry.get("order")) or 999999,
            entry["topic"].casefold(),
            entry["title"].casefold(),
        )
    )

    category_lookup = existing_category_lookup(site_root)
    categories_by_key: dict[str, dict[str, Any]] = {}

    for index, entry in enumerate(entries):
        key = catalog_category_key(entry["subject"], entry["category"])
        if key in categories_by_key:
            continue

        existing = category_lookup.get(key, {})
        subject_slug = entry["subject"].casefold()
        subject_color = next(
            (info["color"] for info in SUBJECTS.values() if info["name"].casefold() == subject_slug),
            "#365f85",
        )

        categories_by_key[key] = {
            "name": entry["category"],
            "subject": entry["subject"],
            "order": normalize_order(existing.get("order")) or (index + 1) * 10,
            "color": existing.get("color") or subject_color,
            "description": existing.get("description") or f"Raccolte di {entry['category'].lower()}.",
        }

    categories = sorted(
        categories_by_key.values(),
        key=lambda category: (
            subject_order.get(category["subject"], 999999),
            normalize_order(category.get("order")) or 999999,
            category["name"].casefold(),
        ),
    )

    return {
        "catalogGenerator": "generate_exercises.py",
        "subjects": list(SUBJECTS.values()),
        "categories": categories,
        "exercises": entries,
    }


def update_global_catalog(site_root: Path) -> Path:
    catalog_path = site_root / GLOBAL_CATALOG_NAME
    catalog = build_global_catalog(site_root)
