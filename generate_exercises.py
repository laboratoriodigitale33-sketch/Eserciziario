"""
Generatore per una singola sottocategoria di esercizi.

Uso:
    python generate_exercises.py fisica/meccanica/dinamica
    python generate_exercises.py fisica/elettrostatica/coulomb
    python generate_exercises.py matematica/limiti/forme-indeterminate

L'argomento e' sempre un percorso relativo alla root del sito e deve iniziare
con fisica/ oppure matematica/.
"""

